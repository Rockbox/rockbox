        xr = a[28] - xr;    a[28] = xr;

        xr = a[25] - a[17]; a[25] = xr;
        xr = a[21] - xr;    a[21] = xr;
        xr = a[29] - xr;    a[29] = xr;

        xr = a[17] - a[1];  a[17] = xr;
        xr = a[ 9] - xr;    a[ 9] = xr;
        xr = a[25] - xr;    a[25] = xr;
        xr = a[ 5] - xr;    a[ 5] = xr;
        xr = a[21] - xr;    a[21] = xr;
