#include "mkjzboot.h"

int main(int argc, char* argv[])
{
    if(argc <= 1) {
        fprintf(stderr, "Not enough arguments\n");
        return 1;
    }

    if(!strcmp(argv[1], "fiiom3k"))
        return mkboot_fiiom3k(argc-1, &argv[1]);
    else {
        fprintf(stderr, "Unknown model '%s'\n", argv[1]);
        return 1;
    }
}
