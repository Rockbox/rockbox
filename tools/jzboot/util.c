uint8_t* loadfile(const char* filename, size_t* length)
{
    FILE* f = NULL;
    uint8_t* outbuf = NULL;
    long size;

    f = fopen(filename, "rb");
    if(!f) {
        fprintf(stderr, "Can't open file for reading: %s\n", filename);
        goto err;
    }

    fseek(f, 0, SEEK_END);
    size = ftell(f);
    fseek(f, 0, SEEK_SET);
    if(size < 0) {
        fprintf(stderr, "Cannot read size of file: %s\n", filename);
        goto err;
    }

    outbuf = malloc(size);
    if(!outbuf) {
        fprintf(stderr, "Out of memory\n");
        goto err;
    }

    if(fread(outbuf, size, 1, f) != 1) {
        fprintf(stderr, "Error reading file: %s\n", filename);
        goto err;
    }

    *length = size;

  out:
    if(f)
        fclose(f);
    return outbuf;

  err:
    if(outbuf)
        free(outbuf);
    outbuf = NULL;
    goto out;
}

uint8_t* uclpack(const uint8_t* buf, size_t size, size_t* outsize)
{
    /* The following formula comes from the UCL documentation */
    size_t max_size = size + (size / 8) + 256;

    /* Allocate output buffer */
    uint8_t* outbuf = malloc(max_size);
    if(!outbuf) {
        fprintf(stderr, "Out of memory\n");
        return NULL;
    }

    /* Compress entire image as one block */
    ucl_uint outsize_ucl;
    int r = ucl_nrv2e_99_compress((const ucl_bytep)buf, size,
                                  (ucl_bytep)outbuf, &outsize_ucl,
                                  0, 10, NULL, NULL);

    if(r != UCL_E_OK || outsize_ucl > max_size) {
        free(outbuf);
        fprintf(stderr, "internal error: UCL compression failed (%d)\n", r);
        return NULL;
    }

    *outsize = outsize_ucl;
    return outbuf;
}

