enum plugin_status plugin_start(struct plugin_api* api, void* parameter)
{
    int button;
    bool exit = 0;

    TEST_PLUGIN_API(api);
    (void)(parameter);
    struct plugin_api* rb = api;

 	 rb->remote_bitmap(rockbox112x37,10,14,112,37, true);
  	 rb->remote_update();

    while(!exit)
    {
        button = rb->button_get(false);
        switch(button)
        {
            case BUTTON_UP:
                rb->remote_backlight_off();
                break;
            case BUTTON_DOWN:
                rb->remote_backlight_on();
                break;              
            case BUTTON_OFF:
                exit = 1;
         }
     }
     
    return PLUGIN_OK;
}

